def compute_relevance_score(row):
    title = clean_text(row.get("title", ""))
    summary = clean_text(row.get("summary", ""))
    content = clean_text(row.get("content", ""))

    title_text = title.lower()
    summary_text = summary.lower()
    content_text = content.lower()
    full_text = f"{title_text} {summary_text} {content_text}"

    score = 0.0

    # Strong penalty for noise articles
    if contains_any_keyword(full_text, GOLD_NOISE_KEYWORDS):
        return 0.05

    # Direct gold in title is strongest
    if contains_any_keyword(title_text, DIRECT_GOLD_KEYWORDS):
        score += 0.60

    # Direct gold in summary/content
    if contains_any_keyword(summary_text, DIRECT_GOLD_KEYWORDS):
        score += 0.20

    if contains_any_keyword(content_text, DIRECT_GOLD_KEYWORDS):
        score += 0.20

    # Domestic gold
    if contains_any_keyword(full_text, DOMESTIC_GOLD_KEYWORDS):
        score += 0.15

    # Macro relevance
    macro_count_title = count_keywords(title_text, MACRO_GOLD_KEYWORDS)
    macro_count_full = count_keywords(full_text, MACRO_GOLD_KEYWORDS)

    if macro_count_title > 0:
        score += min(0.25, macro_count_title * 0.08)

    if macro_count_full > 0:
        score += min(0.25, macro_count_full * 0.04)

    # Very short generic Reuters/category pages should be penalized
    generic_noise_titles = [
        "breaking international news",
        "global market headlines",
        "top energy headlines",
        "stock market news",
    ]

    if contains_any_keyword(title_text, generic_noise_titles):
        score = min(score, 0.15)

    return round(min(max(score, 0.0), 1.0), 4)


