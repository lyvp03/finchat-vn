def build_sentiment_input(row):
    title = clean_text(row.get("title", ""))
    summary = clean_text(row.get("summary", ""))

    text = f"{title}. {summary}".strip()

    if len(text) < 10:
        text = clean_text(row.get("content", ""))[:1000]

    return text[:1500]

def label_to_score(label, confidence):
    label = str(label).lower()

    if "positive" in label:
        return float(confidence)

    if "negative" in label:
        return -float(confidence)

    if "neutral" in label:
        return 0.0

    # fallback cho một số model dùng LABEL_0, LABEL_1, LABEL_2
    if label == "label_2":
        return float(confidence)
    if label == "label_0":
        return -float(confidence)
    if label == "label_1":
        return 0.0

    return 0.0

def predict_model_sentiment(row):
    text = build_sentiment_input(row)

    if not text:
        return {
            "model_sentiment_label": "neutral",
            "model_sentiment_confidence": 0.0,
            "sentiment_score": 0.0,
            "sentiment_model": "none",
        }

    language = clean_text(row.get("language", "")).lower()

    try:
        if language == "en":
            result = finbert_sentiment(text)[0]
            model_name = "finbert"
        else:
            result = multi_sentiment(text)[0]
            model_name = "multilingual_sentiment"

        label = result["label"]
        confidence = float(result["score"])
        score = label_to_score(label, confidence)

        # Không relevant thì kéo về gần neutral
        if not row.get("is_relevant", False):
            score *= 0.3

        return {
            "model_sentiment_label": label,
            "model_sentiment_confidence": confidence,
            "sentiment_score": round(max(min(score, 1.0), -1.0), 4),
            "sentiment_model": model_name,
        }

    except Exception as e:
        return {
            "model_sentiment_label": "error",
            "model_sentiment_confidence": 0.0,
            "sentiment_score": 0.0,
            "sentiment_model": str(e)[:80],
        }

