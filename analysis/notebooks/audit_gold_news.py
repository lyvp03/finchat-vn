def normalize_url(url):
    """
    Normalize URL for stable deduplication.

    MVP rule:
    - Convert NaN/None to empty string
    - Strip spaces
    - Remove query string
    - Remove URL fragment
    """
    if pd.isna(url):
        return ""

    url = str(url).strip()

    if not url:
        return ""

    url = url.split("?")[0]
    url = url.split("#")[0]

    return url

def sha256_text(text):
    """
    Generate SHA256 hash from text.
    """
    if pd.isna(text):
        text = ""

    text = str(text)
    return hashlib.sha256(text.encode("utf-8")).hexdigest()

