def compute_quality_score(row):
    title = clean_text(row.get("title", ""))
    summary = clean_text(row.get("summary", ""))
    content = clean_text(row.get("content", ""))
    source_name = clean_text(row.get("source_name", ""))
    source_type = clean_text(row.get("source_type", "")).lower()
    published_at = row.get("published_at_parsed", pd.NaT)

    title_lower = title.lower()
    content_len = len(content)

    # Reuters/RSS rule: useful for macro overview, but weak for deep RAG
    is_reuters_rss = (
        source_name.lower() == "reuters"
        or source_type == "rss"
    )

    if is_reuters_rss and content_len < 200:
        score = 0.30

        if contains_any_keyword(title_lower, DIRECT_GOLD_KEYWORDS):
            score += 0.10

        if contains_any_keyword(title_lower, MACRO_KEYWORDS):
            score += 0.05

        if pd.notna(published_at):
            score += 0.05

        return round(min(score, 0.50), 4)

    # General full-article rule
    score = 0.0

    if len(title) > 0:
        score += 0.20

    if len(summary) > 0:
        score += 0.10

    if pd.notna(published_at):
        score += 0.10

    if title and content and title.strip() != content.strip():
        score += 0.10

    if source_name:
        score += 0.10

    if content_len >= 800:
        score += 0.40
    elif content_len >= 200:
        score += 0.25
    elif content_len >= 50:
        score += 0.10
    else:
        score += 0.00

    return round(min(max(score, 0.0), 1.0), 4)

