"""### 10. Impact Score

In this step, we compute the potential impact level of each article on gold price.

The score ranges from 0.0 to 1.0:

- 0.0 = almost no impact
- 1.0 = very high impact

Impact score is different from sentiment score:

- sentiment_score measures direction
- impact_score measures strength
"""

def compute_impact_score(row):
    event_type = row.get("event_type", "other")
    relevance_score = float(row.get("relevance_score", 0.0) or 0.0)
    quality_score = float(row.get("quality_score", 0.0) or 0.0)
    is_relevant_value = bool(row.get("is_relevant", False))

    # Base score by event type
    event_base_impact = {
        "fed_policy": 0.85,
        "inflation_data": 0.85,
        "usd_movement": 0.80,
        "bond_yield": 0.80,
        "geopolitical_risk": 0.75,
        "central_bank_demand": 0.75,
        "tariff_trade": 0.65,
        "gold_price_update": 0.65,
        "domestic_market": 0.60,
        "stock_market_risk": 0.45,
        "economic_growth": 0.45,
        "other": 0.20,
    }

    impact = event_base_impact.get(event_type, 0.20)

    # Relevance adjustment
    impact = impact * (0.6 + 0.4 * relevance_score)

    # Quality adjustment
    if quality_score < 0.35:
        impact *= 0.6
    elif quality_score < 0.50:
        impact *= 0.85

    # Non-relevant articles should have low impact
    if not is_relevant_value:
        impact = min(impact, 0.20)

    return round(min(max(impact, 0.0), 1.0), 4)

