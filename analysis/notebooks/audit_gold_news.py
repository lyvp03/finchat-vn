def mark_similar_title_duplicates(df, threshold=90):
    """
    Mark articles as duplicate if their titles are very similar on the same date.

    This is an MVP implementation for notebook use.
    """
    df = df.copy()

    for published_date, group in tqdm(df.groupby("published_date"), desc="Checking similar titles"):
        seen_titles = []

        for idx, row in group.iterrows():
            title = clean_text(row.get("title", "")).lower()

            if not title:
                continue

            already_duplicate = bool(df.at[idx, "is_duplicate"])
            if already_duplicate:
                continue

            matched = False

            for seen_title in seen_titles:
                score = fuzz.ratio(title, seen_title)

                if score >= threshold:
                    df.at[idx, "is_duplicate"] = True
                    df.at[idx, "duplicate_reason"] = "similar_title_same_day"
                    matched = True
                    break

            if not matched:
                seen_titles.append(title)

    return df


