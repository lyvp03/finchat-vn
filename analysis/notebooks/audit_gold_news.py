    text = build_article_text(row)

    # Prefer domestic market if local symbols appear
    symbols = set(row.get("symbols", []))
    tags = set(row.get("tags", []))

    if any(symbol in symbols for symbol in ["SJC", "DOJI", "PNJ", "BTMC", "VND"]):
        return "domestic_market"

    if "fed" in tags:
        return "fed_policy"

    if "cpi" in tags or "pce" in tags or "inflation" in tags:
        return "inflation_data"

    if "usd" in tags or "dxy" in tags:
        return "usd_movement"

    if "bond_yield" in tags:
        return "bond_yield"

    if "geopolitical" in tags or "safe_haven" in tags:
        return "geopolitical_risk"

    if "tariff" in tags:
        return "tariff_trade"

    if "central_bank" in tags:
        return "central_bank_demand"

    if "stock_market" in tags:
        return "stock_market_risk"

    # Fallback by keyword rules
    for event_type in EVENT_TYPE_PRIORITY:
        keywords = EVENT_TYPE_RULES.get(event_type, [])
        if contains_any_keyword(text, keywords):
            return event_type

    return "other"

